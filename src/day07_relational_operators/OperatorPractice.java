package day07_relational_operators;

public class OperatorPractice {

    public static void main (String [] arg ){

        int num = 10;
        num++; //post increment |no othe code, just incremented by 1|
        System.out.println(num); //11

        num--; // post-decrement
        System.out.println(num); //10

        System.out.println(num++); //10 -- > post-increment, first print it then update, my num = 11
        System.out.println(num++);//11, but my num = 12
        System.out.println(++num); //13  pre increment, we update if first --> 13





    }
}
