package day05_variables;

public class ConateExample {

    public static void main (String [] args){

        System.out.println(4 + 5); //9
        System.out.println(4 + 5 + "Hello"); //9Hello
                   // int + int = String
                  // Int + String
                 // String

        System.out.println("Hello" + 4 + 9); // Hello49

                //String + int + int
               // String + int
              //String

        System.out.println(9 + 10 + "Hello" + 8 + 5); // before String it will be sum, after only numbers
    }


}
